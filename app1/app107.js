let x = 5
let y = 3;

//Decrement
console.log(`--y = ${--y}`)
console.log(`y-- = ${y--}`)
console.log(`y = ${y}`)

//increment
console.log(`++x = ${++x}`)
console.log(`x++ = ${x++}`)

//remainder
console.log(`x % y = ${x % y}`)

//Muliplication
console.log(`x * y = ${x * y}`)

//subtraction
console.log(`x - y = ${x - y}`)

//addition
console.log(`x + y = ${x + y}`)